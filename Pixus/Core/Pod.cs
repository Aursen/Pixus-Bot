using System;
using System.Collections.Generic;
using System.Text;
using System.Drawing;

namespace Pixus.Core
{
    class Pod
    {
        //=========================================================================================================================
        //                                                      attributs
        //=========================================================================================================================
        public const int PodBarX = 438;
        public const int PodBarEndX = 533;
        public const int PodBarY = 317;
        public const int PodBarWidth = PodBarEndX - PodBarX;

        //=========================================================================================================================
        //                                                      propriétés
        //=========================================================================================================================
        public int PercentValue { get; set; }

        //=========================================================================================================================
        //                                                      constr.
        //=========================================================================================================================
        public Pod()
        {
            this.PercentValue = 0;
        }

        //=========================================================================================================================
        //                                                      méthodes
        //=========================================================================================================================

        // isFull(...) : retourne true si on est full pod si nn false
        public bool isFull()
        {
            return this.PercentValue >= 99; // %
        }
    }
}
